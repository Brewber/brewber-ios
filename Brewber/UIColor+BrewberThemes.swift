import UIKit

extension UIColor {
    
    class func vividRedColor() -> UIColor {
        return UIColor(red: 1.0, green: 45.0 / 255.0, blue: 85.0 / 255.0, alpha: 1.0)
    }
    
    class func vividCyanColor() -> UIColor {
        return UIColor(red: 45.0 / 255.0, green: 1.0, blue: 215.0 / 255.0, alpha: 1.0)
    }
    
    class func gray246Color() -> UIColor {
        return UIColor(white: 246.0 / 255.0, alpha: 1.0)
    }
    
    class func gray226Color() -> UIColor {
        return UIColor(white: 226.0 / 255.0, alpha: 1.0)
    }
    
    class func gray204Color() -> UIColor {
        return UIColor(white: 204.0 / 255.0, alpha: 1.0)
    }
    
    class func gray185Color() -> UIColor {
        return UIColor(white: 185.0 / 255.0, alpha: 1.0)
    }
    
    class func facebookBlue() -> UIColor {
        return UIColor(red: 59.0 / 255.0, green: 89.0 / 255.0, blue: 152.0 / 255.0, alpha: 1.0)
    }
    
}
